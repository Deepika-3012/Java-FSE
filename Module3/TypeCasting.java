public class ex7 {
    public class TypeCasting {
    public static void main(String[] args) {
        double d = 9.78;
        int i = (int) d;
        System.out.println("Double: " + d);
        System.out.println("Cast to int: " + i);

        int j = 5;
        double e = j;
        System.out.println("Int: " + j);
        System.out.println("Cast to double: " + e);
    }
}

}
